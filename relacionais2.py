numero1 = input("Digite um numero: ")
numero1 = int(numero1)

numero2 = input("Digite um segundo numero: ")
numero2 = int(numero2)

if(numero1 == numero2):
    print("O numero %d eh igual a %d. " % (numero1, numero2))
if(numero1 != numero2):
    print("O numero %d eh diferente de %d. " % (numero1, numero2))
if(numero1 < numero2):
    print("O numero %d eh menor que %d. " % (numero1, numero2))
if(numero1 > numero2):
    print("O numero %d eh maior que %d. " % (numero1, numero2))
